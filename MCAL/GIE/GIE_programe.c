/******************************************************************************
   Syntax          : void GIE_voidEnable(void)
   Description     : Set Global Interrupt Enable
   Parameters      : -
   Return value:   : void
*******************************************************************************/

void GIE_voidEnable(void)
{
	SET_BIT(SREG,SREG_I);
}
/******************************************************************************
   Syntax          : void GIE_voidEnable(void)
   Description     : Set Global Interrupt Disable
   Parameters      : -
   Return value:   : void
*******************************************************************************/
void GIE_voidDisable(void)
{
	CLEAR_BIT(SREG,SREG_I);
}
